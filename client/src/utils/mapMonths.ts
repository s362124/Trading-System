export function mapMonths(monthLabel:string){
    
    switch (monthLabel) {
        case "SEP":
            return 9;
            break;
        case "OCT":
            return 10;
            break;
        case "NOV":
            return 11;
            break;
        case "DEC":
            return 12;
            break;
        case "JAN":
            return 1;
            break;
        case "FEB":
            return 2;
            break;
        case "MARS":
            return 3;
            break;  
        case "AVR":
            return 4;
            break;
        case "MAI":
            return 5;
            break;
        case "JUN":
            return 6;
            break;
        case "JUL":
            return 7;
            break;
        case "AOUT":
            return 8;
            break;
    }
}